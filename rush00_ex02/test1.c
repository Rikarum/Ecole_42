#include <unistd.h>

void	ft_putchar(char a)
{
	write (1, &a, 1);
}

void	rush(int x, int y)
{
	int	p;
	int	r;

	r = 1;
	while (r <= y && x > 0)
	{
		p = 1;
		while (p <= x)
		{
			if ((p == 1 && r == 1) || (p == x && r == 1))
				ft_putchar('A');
			else if ((p == 1 && r == y) || (p == x && r == y))
				ft_putchar('C');
			else if ((p == 1) || (p == x) || (r == 1) || (r == y))
				ft_putchar('B');
			else
				ft_putchar(' ');
			p++;
		}
		ft_putchar('\n');
		r++;
	}
}

int	main(void)
{
	rush(123, 42);
	return (0);
}
