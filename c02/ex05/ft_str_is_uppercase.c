int	ft_str_is_uppercase(char *str)
{
	int	r;

	r = 0;
	while (str[r] != '\0')
	{
		if (!(str[r] >= 'A' && str[r] <= 'Z'))
		{
			return (0);
		}
		r++;
	}
	return (1);
}
