char	*ft_strlowcase(char *str)
{
	int	r;

	r = 0;
	while (str[r] != '\0')
	{
		if (str[r] >= 'A' && str[r] <= 'Z')
			str[r] += 32;
		r++;
	}
	return (str);
}
